#!/bin/sh

set -e

# -----------------------------------------------------------------------------
# app_init.sh
#
# 角色:
#   这是 rootfs 的 rcS 在挂载 /system 之后调用的第一个 rebuild 脚本。
#
# 设计原则:
#   这里故意保持极简，只做“打印入口日志 + 跳转到真正启动脚本”两件事。
#   这样做的好处是:
#   1. 一眼就能从串口日志看出 /system 启动链有没有执行到
#   2. 如果后面 start.sh 失败，也不会和 rootfs 本身的启动问题混在一起
# -----------------------------------------------------------------------------
echo "app_init.sh start"

# 将控制权交给真正的 T23 初始化脚本。
/system/init/start.sh
